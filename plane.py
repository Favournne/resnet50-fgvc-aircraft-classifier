class FGVCAircraftDataset(Dataset):
    def __init__(self, data_dir, split_file, class_to_idx, transform=None):
        self.images_dir = os.path.join(data_dir, "images")
        self.transform = transform
        self.image_labels = []

        with open(os.path.join(data_dir, split_file), 'r') as f:
            for line in f:
                if line.strip():
                    img_id, class_name = line.strip().split(' ', 1)
                    self.image_labels.append((f"{img_id}.jpg", class_to_idx[class_name]))

    def __len__(self): return len(self.image_labels)
    def __getitem__(self, idx):
        img_name, label = self.image_labels[idx]
        image = Image.open(os.path.join(self.images_dir, img_name)).convert("RGB")
        if self.transform:
          image = self.transform(image)
        return image, label

