    mean = np.array([0.485, 0.456, 0.406])
    std = np.array([0.229, 0.224, 0.225])
    np_img = std * np_img + mean

    # Clip pixel values so they stay strictly between 0 and 1
    np_img = np.clip(np_img, 0, 1)
    return np_img

